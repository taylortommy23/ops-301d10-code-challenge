# Sample values for a and b
a = 5
b = 10

# Example 1: if statement with logical conditionals and elif
if a == b:
    print("a is equal to b")
elif a < b:
    print("a is less than b")
elif a > b:
    print("a is greater than b")
else:
    print("None of the conditions are met")

# Example 2: if statement with logical conditionals and else
if a != b:
    print("a is not equal to b")
else:
    print("a is equal to b")
